package Operators;

public class ArithmeticExercise2 {
  public static void main (String[] args) {
    int firstNum = 125;
    int secondNum = 24;

    System.out.println(firstNum + secondNum);
    System.out.println(firstNum - secondNum);
    System.out.println(firstNum * secondNum);
    System.out.println(firstNum / secondNum);
    System.out.println(firstNum % secondNum);

  }
}
